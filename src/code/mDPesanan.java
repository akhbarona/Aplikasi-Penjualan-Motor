    private void tbHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbHapusActionPerformed
        // TODO add your handling code here:
         try{
            String sql = "DELETE FROM tabel_pesan WHERE no ='"+tNo_Pesanan.getText()+"'";
            java.sql.Connection conn = (Connection)koneksi.koneksiDB.myDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.executeUpdate();
            JOptionPane.showMessageDialog(rootPane, "Berhasil dihapus!");
            showData();
            bersihkanlayar();
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Gagal dihapus!"+ex.getMessage());
        }
    }//GEN-LAST:event_tbHapusActionPerformed

