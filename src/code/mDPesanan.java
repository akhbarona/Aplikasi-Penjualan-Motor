    private void showData(){
        
        DefaultTableModel tabelP = new DefaultTableModel(); 
        tabelP.addColumn("No.");
        tabelP.addColumn("First Name");
        tabelP.addColumn("Last Name");
        tabelP.addColumn("Username");
        tabelP.addColumn("Kode Motor");
        tabelP.addColumn("Nama Motor");
        tabelP.addColumn("Warna");
        tabelP.addColumn("Tahun");
        tabelP.addColumn("Harga");
        tabelP.addColumn("Jumlah");
        tabelP.addColumn("Total");
        tabelP.addColumn("Metode");
        tabelP.addColumn("Transfer");
        tabelP.addColumn("Kembalian");
        try{
           
            String sql = "SELECT * FROM tabel_pesan";
            java.sql.Connection conn = (Connection)koneksi.koneksiDB.myDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()){
                tabelP.addRow(new Object[]{res.getInt(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8),res.getString(9),res.getString(10),res.getString(11),res.getString(12),res.getString(13),res.getString(14)});
                
            }tabelPemesanan.setModel(tabelP);
        }catch (SQLException ex){
            System.out.println("Error = " + ex.getMessage());
        }
    }
        

