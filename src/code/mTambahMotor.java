    private void showData(){
     DefaultTableModel tabelM = new DefaultTableModel();
     tabelM.addColumn("Kode Motor");
     tabelM.addColumn("Nama Motor");
     tabelM.addColumn("Warna");
     tabelM.addColumn("Tahun");
     tabelM.addColumn("Merk");
     tabelM.addColumn("Harga");
     tabelM.addColumn("Username Penginput");
     
     try{
         String sql = "SELECT * FROM daftarmotor ORDER by kode_motor DESC";
         java.sql.Connection conn = (Connection)koneksi.koneksiDB.myDB();
         java.sql.Statement stm = conn.createStatement();
         java.sql.ResultSet rs = stm.executeQuery(sql);
         while(rs.next())
         {
             tabelM.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7),});
         }tabelMotor.setModel(tabelM);
     }catch(SQLException ex){
         JOptionPane.showMessageDialog(rootPane, "Gagal disimpan!"+ex.getMessage());
     }
 }

