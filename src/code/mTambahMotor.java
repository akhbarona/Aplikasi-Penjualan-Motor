    private void tbTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbTambahActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO daftarmotor (kode_motor,nama_motor,warna_motor,tahun,merk_motor,harga_motor,username_pegawai)"+"VALUES(?,?,?,?,?,?,?)";
            java.sql.Connection conn = (Connection)koneksi.koneksiDB.myDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, tKodeMotor.getText().trim());
            pstm.setString(2, tNamaMotor.getText().trim());
            pstm.setString(3, tWarnaMotor.getText().trim());
            pstm.setString(4, tTahun.getText().trim());
            pstm.setString(5, tMerk.getText().trim());
            pstm.setString(6, tHargaMotor.getText().trim());
            pstm.setString(7, tUsernamePegawai.getText().trim());
            pstm.execute();
            JOptionPane.showMessageDialog(rootPane, "Berhasil disimpan!");
            showData();
            bersihkanlayar();
        
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Gagal disimpan!"+ex.getMessage());
        }
    }//GEN-LAST:event_tbTambahActionPerformed

